
public class Block {
 private boolean valid;
 private int tag;
 private String data;
public boolean isValid() {
	return valid;
}
public void setValid(boolean valid) {
	this.valid = valid;
}
public int getTag() {
	return tag;
}
public void setTag(int tag) {
	this.tag = tag;
}
public String getData() {
	return data;
}
public void setData(String data) {
	this.data = data;
}
 
}
